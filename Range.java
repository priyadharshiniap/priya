import java.util.*;
import java.lang.*;
import java.io.*;
class Range
{
	public static void main (String[] args) throws java.lang.Exception
	{
		int a;
    	Scanner sc=new Scanner(System.in);
    	a=sc.nextInt();
    	if(a>=1&&a<=10)
    	{
    		System.out.println("yes");
    	}
    	else
    	{
    		System.out.println("no");
    	}
	}
}
